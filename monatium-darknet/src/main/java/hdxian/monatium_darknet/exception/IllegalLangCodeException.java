package hdxian.monatium_darknet.exception;

public class IllegalLangCodeException extends CustomBusinessLogicException {

    public IllegalLangCodeException() {
        super();
    }

    public IllegalLangCodeException(String message, Throwable cause) {
        super(message, cause);
    }

    public IllegalLangCodeException(Throwable cause) {
        super(cause);
    }

    public IllegalLangCodeException(String message) {
        super(message);
    }

}
